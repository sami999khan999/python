# 2D lists

# A 2D list, also known as a list of lists, is essentially a list where each element is itself a list. This creates a two-dimensional structure, similar to a matrix or table.

list = [
    [10, 20, 30, 40, 50],
    ["red", "green", "blue", "yellow", "purple"],
    [42, "hello", 3.14, True, None],
]

print(list)
print(list[1])
print(list[1][3])
