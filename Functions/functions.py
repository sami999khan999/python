# A function in Python is a block of reusable code that performs a specific task. Functions help break a program into smaller, modular parts, making it easier to read, maintain, and debug. You can pass data to a function (called arguments), and the function can return a result (using the return statement).


def greet():
    print("Hello, world!")


greet()


def name(name):
    print("Hello,", name)


name("Sami")


def sum(num1, num2):
    print(num1 + num2)


sum(5, 10)
