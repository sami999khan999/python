# ---------------------------
# Addition
# ---------------------------
# Adding two numbers using the "+" operator.
# This operation simply returns the sum of the two numbers.
addition_result = 5 + 3
print(f"Addition: {addition_result}")  # Output: 8

# Another example of using addition directly in a print statement:
print(f"Addition: {10 + 15}")  # Output: 25


# ---------------------------
# Subtraction
# ---------------------------
# Subtracting one number from another using the "-" operator.
# It returns the difference, which is the first number minus the second.
subtraction_result = 8 - 3
print(f"Subtraction: {subtraction_result}")  # Output: 5

# Subtraction can also be used inline like this:
print(f"Subtraction: {20 - 7}")  # Output: 13


# ---------------------------
# Multiplication
# ---------------------------
# Multiplying two numbers using the "*" operator.
# This operation gives the product of the two numbers.
multiplication_result = 4 * 5
print(f"Multiplication: {multiplication_result}")  # Output: 20

# Another example of multiplication, done inline in a print statement:
print(f"Multiplication: {7 * 6}")  # Output: 42


# ---------------------------
# Division
# ---------------------------
# Dividing one number by another using the "/" operator.
# The result is a float value representing the quotient.
division_result = 10 / 2
print(f"Division: {division_result}")  # Output: 5.0

# Division also works directly inside print statements:
print(f"Division: {15 / 3}")  # Output: 5.0


# ---------------------------
# Floor Division
# ---------------------------
# Performing floor division with "//", which returns an integer result.
# It divides the numbers and rounds the quotient down to the nearest whole number.
floor_division_result = 10 // 3
print(f"Floor Division: {floor_division_result}")  # Output: 3

# Example of floor division inline:
print(f"Floor Division: {15 // 4}")  # Output: 3


# ---------------------------
# Modulus
# ---------------------------
# Finding the remainder of division using the "%" operator.
# The result is what's left after dividing the numbers.
modulus_result = 10 % 3
print(f"Modulus: {modulus_result}")  # Output: 1

# Modulus is helpful for checking if a number is divisible by another:
print(f"Modulus: {20 % 6}")  # Output: 2


# ---------------------------
# Exponentiation
# ---------------------------
# Raising a number to the power of another using the "**" operator.
# This operation multiplies the base number by itself exponent number of times.
exponentiation_result = 2**3
print(f"Exponentiation: {exponentiation_result}")  # Output: 8

# Inline exponentiation for quick calculations:
print(f"Exponentiation: {5 ** 2}")  # Output: 25
