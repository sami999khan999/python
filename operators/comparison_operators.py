x = 10

# ---------------------------
# Equal to
# ---------------------------
# Checks if two values are equal.
is_equal = x == 8.0
print(f"Equal to: {is_equal}")  # Output: True

# ---------------------------
# Not Equal to
# ---------------------------
# Checks if two values are not equal.
is_not_equal = x != 8.0
print(f"Not Equal to: {is_not_equal}")  # Output: False

# ---------------------------
# Greater than
# ---------------------------
# Checks if left operand is greater than right operand.
is_greater = x > 5
print(f"Greater than: {is_greater}")  # Output: True

# ---------------------------
# Less than
# ---------------------------
# Checks if left operand is less than right operand.
is_less = x < 10
print(f"Less than: {is_less}")  # Output: True

# ---------------------------
# Greater than or Equal to
# ---------------------------
# Checks if left operand is greater than right operand.
is_greater_equal = x >= 8.0
print(f"Greater than or Equal to: {is_greater_equal}")  # Output: True

# ---------------------------
# 6. Less than or Equal to
# ---------------------------
# Checks if left operand is less than or equal to the right operand.
is_less_equal = x <= 8.0
print(f"Less than or Equal to: {is_less_equal}")  # Output: True
