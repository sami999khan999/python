# ---------------------------
# in
# ---------------------------
# Returns True if the specified value is found in the sequence (e.g., list, string).
my_list = [1, 2, 3, 4, 5]
is_in_list = 3 in my_list
print(f"In: {is_in_list}")  # Output: True

# ---------------------------
# not in
# ---------------------------
# Returns True if the specified value is not found in the sequence.
is_not_in_list = 6 not in my_list
print(f"Not In: {is_not_in_list}")  # Output: True
