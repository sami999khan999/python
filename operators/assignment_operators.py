# ---------------------------
# Simple Assignment
# ---------------------------
# Assigning a value to a variable.
x = 10

# ---------------------------
# Add and Assign
# ---------------------------
# Adds right operand to the left operand and assigns the result to the left operand.
x += 5  # Equivalent to x = x + 5
print(f"Add and Assign: {x}")  # Output: 15

# ---------------------------
# Subtract and Assign
# ---------------------------
x -= 3  # Equivalent to x = x - 3
print(f"Subtract and Assign: {x}")  # Output: 12

# ---------------------------
# Multiply and Assign
# ---------------------------
x *= 2  # Equivalent to x = x * 2
print(f"Multiply and Assign: {x}")  # Output: 24

# ---------------------------
# Divide and Assign
# ---------------------------
x /= 3  # Equivalent to x = x / 3
print(f"Divide and Assign: {x}")  # Output: 8.0
