# set

# A set is an unordered collection of unique elements in Python. Sets are useful for storing items where each element is distinct and where the order of elements does not matter. Sets support operations like union, intersection, difference, and more, which are based on mathematical set theory.

my_set = set([1, 2, 3, 4])
new_set = {"apple", "orange", "banana", "mango"}

print(my_set)
print(new_set)
print(type(my_set))

for i in my_set:
    print(i)
